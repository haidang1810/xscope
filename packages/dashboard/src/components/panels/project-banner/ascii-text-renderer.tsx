const FALLBACK: string[] = ["‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą", "‚Ėą   ‚Ėą", "‚Ėą   ‚Ėą", "‚Ėą   ‚Ėą", "‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą"];

interface Props {
  text: string;
  /** Max chars before truncating */
  maxChars?: number;
}

/** Renders text as 5-row ASCII block art using span elements. */
export function AsciiTextRenderer({ text, maxChars = 18 }: Props) {
  const upper = text.toUpperCase().slice(0, maxChars);
  const chars = upper.split("");

  // Build 5 rows by concatenating each character's row
  const rows = Array.from({ length: 5 }, (_, row) =>
    chars.map((ch) => (BLOCK_FONT[ch] ?? FALLBACK)[row]).join(""),
  );

  return (
    <pre
      aria-label={text}
      style={{
        fontFamily: "var(--font-mono)",
        fontSize: "clamp(6px, 1vw, 10px)",
        lineHeight: 1.15,
        letterSpacing: "0.05em",
        background: "linear-gradient(90deg, var(--accent-primary), var(--accent-secondary), var(--accent-success), var(--accent-primary))",
        backgroundSize: "300% 100%",
        WebkitBackgroundClip: "text",
        WebkitTextFillColor: "transparent",
        backgroundClip: "text",
        animation: "ascii-rainbow 4s linear infinite",
        userSelect: "none",
        margin: 0,
        overflow: "hidden",
        whiteSpace: "pre",
      }}
    >
      {rows.join("\n")}
    </pre>
  );
}
